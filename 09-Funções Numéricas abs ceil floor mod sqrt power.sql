--Funções Numéricas abs ceil floor mod sqrt power

--ABS retorna sempre positivo
SELECT ABS(-6.75) FROM DUAL; 

--CEIL retorna o proximo inteiro
SELECT CEIL(6.75) FROM DUAL; 

--FLOOR retorna o anterior inteiro
SELECT FLOOR(6.75) FROM DUAL; 

--MOD retorna o resto de uma divisão
SELECT MOD(10,3) FROM DUAL; 

--POWER executa potenciação
SELECT POWER(6,2) FROM DUAL; 

--SQRT executa raiz quadrada
SELECT SQRT(49) FROM DUAL; 
