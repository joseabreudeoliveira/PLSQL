--Personalizando Data e Hora com SYSDATE

SELECT SYSDATE FROM DUAL; --15/12/20
SELECT TO_CHAR(SYSDATE,'dd/mm/yyyy') FROM DUAL; --15/12/2020
SELECT TO_CHAR(SYSDATE,'dd/mm/yyyy hh24:mi:ss') FROM DUAL; --15/12/2020 10:39:43
SELECT TO_CHAR(SYSDATE,'dd/mm/yyyy hh24:mi:ss') AS DATA_HORA FROM DUAL; --15/12/2020 10:39:43
SELECT TO_CHAR(SYSDATE,'dd/mon/yyyy hh24:mi:ss') AS DATA_HORA FROM DUAL; --15/dez/2020 10:45:35
SELECT TO_CHAR(SYSDATE,'dd/month/yyyy hh24:mi:ss') AS DATA_HORA FROM DUAL; --15/dezembro /2020 10:46:47

